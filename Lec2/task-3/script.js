function add(x,y){
    return x+y;
}
console.log(add(5,10));//output=15

function subtract(x,y){
    return x-y;
}
console.log(subtract(10,4));//output=6

function multiply(x,y){
    return x*y;
}
console.log(multiply(3,7));//output=21

function divide(x,y){
    return x/y;
}
console.log(divide(20,5));//output=4